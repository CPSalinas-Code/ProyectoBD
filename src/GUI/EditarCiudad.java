    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        //datosCampos = new ArrayList<>();
        String []datos = new String [5];
        String testNumPisos = txtNumHab.getText();
        int numPisos;
        try{
            numPisos = Integer.parseInt(testNumPisos);
            if(numPisos<0){
                JOptionPane.showMessageDialog(null, "Error al ingresar el numero de habitantes, no negativo");
            }else{
                datos[0]= txtIDCiudad.getText();
                datos[1]= txtNombreCiudad.getText();
                datos[2]= txtEstado.getText();
                datos[3]= txtPais.getText();
                datos[4]= txtNumHab.getText();
                if (admin.updateDatos(datos,nombreTabla)){
                    JOptionPane.showMessageDialog(null, "Datos Modificados correctamente");
                    this.setVisible(false);
                    ventanaTabla.actualizar();
                }else{
                    JOptionPane.showMessageDialog(null, "Error al modificar los Datos del registro o no tiene permisos");
                }
            }
        }catch (Exception e) {
            System.out.println("Debe Ingresar digitos en el Numerde habitantes");
            JOptionPane.showConfirmDialog(null, "Debe Ingresar digitos en el Numerde habitantes");

        }
    }//GEN-LAST:event_btnGuardarActionPerformed

