    public void inicializar(){
        ArrayList datos = new ArrayList<>();
        datos = admin.recuperarDatos(clave,"Ciudades");
        if (datos.isEmpty()){
            JOptionPane.showMessageDialog(null, "No existen datos recuperados");
            //this.setVisible(false);
        }else{
            txtIDCiudad.setText((datos.get(0)).toString());
            txtNombreCiudad.setText((datos.get(1)).toString());
            txtEstado.setText((datos.get(2)).toString());
            txtPais.setText((datos.get(3)).toString());
            txtNumHab.setText((datos.get(4)).toString());
            this.setVisible(true);
        }
        
    }
